// This function is designed to calculate the product of two numbers.
// It is named 'multiplyTwoNumbers' to clearly indicate its purpose.
function multiply(a, b) {
// We use multiplication to obtain the result of number1 multiplied by number2.
// This is a straightforward mathematical operation with a clear result.
  return a * b;
}
// We are calling the function 'multiplyTwoNumbers' with 4 and 5 as arguments.
// These values are chosen to demonstrate the function's ability to multiply two specific
let product = multiply(4, 5);
console.log("Product is: " + product);
